    private void tblWorkOnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblWorkOnMouseClicked

        try{
            int row = tblWorkOn.getSelectedRow();
            String empN = tblWorkOn.getValueAt(row, 1).toString();
            String ProNamee = tblWorkOn.getValueAt(row,3).toString();
            
            cbxEmp.setSelectedItem(empN);
            cbxProject.setSelectedItem(ProNamee);
        
        }
        catch(Exception ex){
        Tools.msgBox(ex.getMessage());}
    }//GEN-LAST:event_tblWorkOnMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
         String StrSearch = "Select * from workon_data where " ;

         if (rdoEmpName.isSelected()){
         StrSearch += "EMPNAME";
         }
         else if(rdoEmpNo.isSelected()){
         StrSearch += "EMPNO";
         }
         else if(rdoProjectName.isSelected()){
         StrSearch += "ProjectName";
         }
         else  {
         StrSearch += "PROJECTNO";
         }
         
         
            StrSearch += " LIKE '%" +   txtSearch.getText()  + "%' ";
            proj.getCustomRows(StrSearch, tblWorkOn);
    }//GEN-LAST:event_btnSearchActionPerformed
    
