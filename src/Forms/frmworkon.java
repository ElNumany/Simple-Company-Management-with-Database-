    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed

        String StrEmpNo = (String) Tools.InputBox("Enter Employee Number");
        String StrProjNo = Tools.InputBox("Enter Project Number").toString();
        
        int Empno = Integer.parseInt(StrEmpNo);
        int EntProNo = Integer.parseInt(StrProjNo);
        
        work.setEMPNO(Empno);
        work.setPROJECTNO(EntProNo);
        
        work.getOneRow(tblWorkOn);
    }//GEN-LAST:event_btnFindActionPerformed

