    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String StrSearch = "SELECT * from project where ";
        if (RdoProjectNo.isSelected()){
        StrSearch+= "PROJECTNO";
        }
        else if (RdoName.isSelected()){
            StrSearch +="PRONAME";
                }
        else if (RdoLocation.isSelected()) {
            StrSearch += "PROLOCATION";
        }
        else {
            StrSearch += "DEPTNO";
        }
            StrSearch += " LIKE '%" + txtSearch.getText() +"%'  ";
        P.getCustomRows(StrSearch, tblProject);
        
    }//GEN-LAST:event_btnSearchActionPerformed
    
