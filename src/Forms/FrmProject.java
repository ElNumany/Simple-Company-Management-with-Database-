    private void setValues(){
    
        int projectNo      = Integer.parseInt(txtProjectNo.getText());
        String projectName = txtProjectName.getText();
        String Location    = txtProjectLocation.getText();
        String DeptName    = cbxDept.getSelectedItem().toString();
        
       int deptNo         = Integer.parseInt(D.getValueByName(DeptName));
       
        //Error Here ^ ////////////
        
        P.setProjectNO(projectNo);
        P.setLocation(Location);
        P.setProjectName(projectName);
        P.setDEPTNO(deptNo);
    }
